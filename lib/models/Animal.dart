class Animal {
  String ?id;
  String ?name;
  String ?animal;
  String ?race;
  String ?number;
  String ?birthdate;
  String ?photo;

  Animal(id, name, animal,race,number,birthdate,photo){
  this.id = id;
  this.name = name;
  this.animal = animal; 
  this.race = race;
  this.number = number;
  this.birthdate = birthdate;
  this.photo = photo;
  }
}